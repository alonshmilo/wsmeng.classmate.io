package com.dt.bean;

public final class AdressInfo {
	private int Adress_ID;
	private String Adress_City;
	private String Adress_Down;
	private String Adress_Number;
	public int getAdress_ID() {
		return Adress_ID;
	}
	public void setAdress_ID(int adress_ID) {
		Adress_ID = adress_ID;
	}
	public String getAdress_City() {
		return Adress_City;
	}
	public void setAdress_City(String adress_City) {
		Adress_City = adress_City;
	}
	public String getAdress_Down() {
		return Adress_Down;
	}
	public void setAdress_Down(String adress_Down) {
		Adress_Down = adress_Down;
	}
	public String getAdress_Number() {
		return Adress_Number;
	}
	public void setAdress_Number(String adress_Number) {
		Adress_Number = adress_Number;
	}
	
}
